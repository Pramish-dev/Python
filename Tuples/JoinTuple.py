#PYTHON- JOIN TUPLES

#JOIN TWO TUPLES
tuple1 = ("a","b","c")
tuple2 = (1,2,3)
tuple3= tuple1+tuple2
print(tuple3)

#OUTPUT: ('a', 'b', 'c', 1, 2, 3)

#MULTIPLY TUPLES
#If you want to multiply the content of a tuple a given number of times, you can use the * operator

fruits = ("apple","banana","cherry")
mytuple = fruits * 2
print(mytuple)

#OUTPUT: ('apple', 'banana', 'cherry', 'apple', 'banana', 'cherry')
