# Python -> ADD DICTIONARY ITEMS

# Adding Items
thisdict ={
    "brand":"Ford",
    "model": "Mustang",
    "year":2024
    }
thisdict["color"] = "red"
print(thisdict)

#OUTPUT: {'brand': 'Ford', 'model': 'Mustang', 'year': 2024, 'color': 'red'}


#UPDATE DICTIONARY -> update()

thisdict ={
    "brand":"Ford",
    "model":"Mustang",
    "year":1964
    }
thisdict.update({"color":"red"})
print(thisdict)

#OUTPUT: {'brand': 'Ford', 'model': 'Mustang', 'year': 1964, 'color': 'red'}








































