# PYTHON - CHANGE DICTIONARY ITEMS

#Change values:

thisdict = {
    "brand": "Ford",
    "model":"Mustang",
    "year": 2024
    }
thisdict["year"]=2010
print(thisdict)

#OUTPUT: {'brand': 'Ford', 'model': 'Mustang', 'year': 2010}


#UPDATE DICTIONARY -> update()

thisdict.update({"year":2020})
print(thisdict)

#OUTPUT: {'brand': 'Ford', 'model': 'Mustang', 'year': 2020}




























































































































